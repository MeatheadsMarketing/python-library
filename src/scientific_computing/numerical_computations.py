import numpy as np

def basic_arithmetic(a, b):
    return np.add(a, b), np.subtract(a, b), np.multiply(a, b), np.divide(a, b)

def logarithm_and_exponential(x):
    return np.log(x), np.exp(x)

def factorial(n):
    return np.math.factorial(n)

def modular_arithmetic(a, b):
    return np.mod(a, b)
